#include "3-calc.h"
int op_add(int a, int b);
int op_sub(int a, int b);
int op_mul(int a, int b);
int op_div(int a, int b);
int op_mod(int a, int b);
/**
 * op_add - asdff
 * @a: sfg
 * @b: sfg
 * Return: sfg
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - fgfg
 * @a: sgffgsfg
 * @b: sfggg
 * Return: sdfg
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - svxcv
 * @a: xcvc
 * @b: xcvxcv
 * Return: cvxcv
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - zxcvzcxv
 * @a: xcvxcv
 * @b: xcvxcv
 * Return: xcvv
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - xcvxcv
 * @a: xcvxcv
 * @b: svxcv
 * Return: zxcvxv
 */
int op_mod(int a, int b)
{
return (a % b);
}
